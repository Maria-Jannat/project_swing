    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed

        String fullName = txtFullName.getText().trim();
        String email = txtEmail.getText().trim();
        String userName = txtUsername.getText().trim();
        String password = txtPassword.getText().trim();

        if (fullName.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter Your Name");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter Your Email Adress");
        } else if (userName.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter A Username");
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter A Password");
        } else {
            SignUp signup = new SignUp(fullName, email, userName, password);
            commonService.save(signup);
            reset();
            new DashboardView().setVisible(true);
            JOptionPane.showMessageDialog(rootPane, "Sign Up Successfully");
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

